package com.pankajranag.rana_gaming.JavaClass;

public class StudentsRegistration {
    public String name, email, pubgname, pubgid, mobileno, address, date, lastname, coins, litecoins, freecoins;


    public StudentsRegistration(String name, String email, String pubgname, String pubgid, String mobileno, String address, String date, String lastname, String coins, String litecoins, String freecoins) {
        this.name = name;
        this.email = email;
        this.pubgname = pubgname;
        this.pubgid = pubgid;
        this.mobileno = mobileno;
        this.address = address;
        this.date = date;
        this.lastname = lastname;
        this.coins = coins;
        this.litecoins = litecoins;
        this.freecoins = freecoins;
    }
}
